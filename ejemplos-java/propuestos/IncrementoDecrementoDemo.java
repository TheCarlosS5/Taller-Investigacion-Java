public class IncrementoDecrementoDemo {
    public static void main(String[] args) {
        int contadorTrad = 5;
        // Forma tradicional
        contadorTrad = contadorTrad + 1;

        int contadorAbbr = 5;
        // Forma abreviada
        contadorAbbr++;

        System.out.println("Tradicional: " + contadorTrad);
        System.out.println("Abreviado: " + contadorAbbr);
    }
}
